    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

       double lado = 0.0;
       double base = 0.0;
       String input1 = "";
       String input2 = "";
       input1 = this.jTextField1.getText(); 
       input2 = this.jTextField2.getText(); 
       
       if(input1 != null && !input1.isEmpty()){
           lado = Double.parseDouble(input1);
       }else{
           lado = 0.0; 
       }
       if(input2 != null && !input2.isEmpty()){
           base = Double.parseDouble(input2);
       }else{
           base = 0.0;
       }
       
       String area = "";
       String perimetro = "";
       calculo calcular = new calculo();
      
       calcular.setLado(lado);
       calcular.setBase(base);      
        
        switch(jComboBox1.getSelectedIndex()){
          case 0:
              break;
          case 1:
              perimetro = Double.toString(calculo.cuadrado_perimetro(lado));               
              area = Double.toString(calculo.cuadrado_area(lado));               
              //Graficas.DibujarCuadrado(tablero.getGraphics(), 20);
              break;
          case 2:
              perimetro = Double.toString(calculo.circulo_perimetro(lado));
              area = Double.toString(calculo.circulo_area(lado));
              break;
          case 3:
              perimetro = Double.toString(calculo.triangulo_perimetro(lado));
              area = Double.toString(calculo.triangulo_area(lado));
              break;
          case 4:
              perimetro = Double.toString(calculo.rectangulo_perimetro(lado,base));
              area = Double.toString(calculo.rectangulo_area(lado,base)); 
              break;
        }         
       this.jLabel7.setText(perimetro);
       this.jLabel8.setText(area);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int lado = 0;
        int base = 0;
        String input1 = "";
        String input2 = "";
        
        input1 = this.jTextField1.getText(); 
        input2 = this.jTextField2.getText();
        
        if(input1 != null && !input1.isEmpty()){
            lado = Integer.parseInt(input1);
        }else{   
            lado = 0; 
        }
        
        if(input2 != null && !input2.isEmpty()){
           base = Integer.parseInt(input2);
        }else{ 
            base = 0;
        }
        
        grafPanel.setLado(lado);
        grafPanel.setBase(base);       
        
        switch(jComboBox1.getSelectedIndex()){
            case 0:
                break;
            case 1:  
                Graficas.CleanPanel(tablero.getGraphics());
                Graficas.DibujarCuadrado(tablero.getGraphics(), lado);            
                break;
            case 2:
                Graficas.CleanPanel(tablero.getGraphics());
                Graficas.DibujarCirculo(tablero.getGraphics(), lado);
                break;
            case 3:
                Graficas.CleanPanel(tablero.getGraphics());
                Graficas.DibujarTriangulo(tablero.getGraphics(), lado);
                break;
            case 4:
                Graficas.CleanPanel(tablero.getGraphics());
                Graficas.DibujarRectangulo(tablero.getGraphics(), base, lado);
                break;
        }
    }//GEN-LAST:event_jButton1ActionPerformed



